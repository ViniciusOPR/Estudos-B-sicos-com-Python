"""
Operadores de comparação (relacionais)
> - Maior - 2 > 1
< - Menor - 1 < 2
>= - Maior ou Igual - 2 >= 2
<= - Menor ou Igual - 2 <= 2
== - Igual - 'a' == 'a'
!= - Diferente - 'a' != 'b'
"""

maior = 2 > 1
menor = 1 < 2
maior_igual = 2 >= 2
menor_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(maior, menor, maior_igual, menor_igual, igual, diferente)